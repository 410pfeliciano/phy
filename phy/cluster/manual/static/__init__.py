import os.path as op


# -----------------------------------------------------------------------------
# Utilities
# -----------------------------------------------------------------------------

def _path(fn):
    curdir = op.dirname(op.realpath(__file__))
    return op.join(curdir, fn)


def _read(fn):
    with open(_path(fn), 'r') as f:
        return f.read()
